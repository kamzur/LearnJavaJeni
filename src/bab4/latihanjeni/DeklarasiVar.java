public class DeklarasiVar {
    
    public static void main(String args[]){
        
        // deklarasi variabel
        int number      = 10;
        char letter     = 'a';
        boolean result  = true;
        String str      = "hello";
        
        // cetak variabel
        System.out.println("Number = " + number);
        System.out.println("letter = " + letter);
        System.out.println("result = " + result);
        System.out.println("str = " + str);
    }
}
